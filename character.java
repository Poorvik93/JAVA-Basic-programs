        byte b1;
        b1 = (byte) a1;
        System.out.println(a1);
        System.out.println(b1);
        System.out.println("char to byte is Explicit Casting\n");

        // char to short ....
        char a2 = 'A';
        short b2;
        b2 = (short) a2;
        System.out.println(a2);
        System.out.println(b2);
        System.out.println("char to short is Explicit Casting\n");

        // char to char....
        char a3 = 'A';
        char b3;
        b3 = a3;
        System.out.println(a3);
        System.out.println(b3);
        System.out.println("char to char is Casting not required \n");

        // char to int ...
        char a4 = 'A';
        int b4;
        b4 = a4;
        System.out.println(a4);
        System.out.println(b4);
        System.out.println("char to int is implicit Casting\n");

        // char to long...
        char a5 = 'A';
        long b5;
        b5 = a5;
        System.out.println(a5);
        System.out.println(b5);
        System.out.println("char to long is implicit Casting\n");

