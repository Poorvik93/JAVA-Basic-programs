        // short to byte ....
        short a1 = 4;
        byte b1;
        b1 = (byte) a1;
        System.out.println(a1);
        System.out.println(b1);
        System.out.println("short to byte is Explicit Casting\n");

        // short to short ....
        short a2 = 10;
        short b2;
        b2 = a2;
        System.out.println(a2);
        System.out.println(b2);
        System.out.println("short to short is implicit Casting\n");

        // short to char....
        short a3 = 10;
        char b3;
        b3 = (char) a3;
        System.out.println(a3);
        System.out.println(b3);
        System.out.println("short to char is Explicit Casting\n");

        // short to int ...
        short a4 = 10;
        int b4;
        b4 = a4;
        System.out.println(a4);
        System.out.println(b4);
        System.out.println("short to int is implicit Casting\n");

        // short to long...
        short a5 = 10;
        long b5;
        b5 = a5;
        System.out.println(a5);
        System.out.println(b5);
        System.out.println("short to long is implicit Casting\n");

        // short to float...
        short a6 = 10;
        float b6;
        b6 = a2;
        System.out.println(a6);
        System.out.println(b6);
        System.out.println("short to float is implicit Casting\n");

