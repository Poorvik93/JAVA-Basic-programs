
public class MultipleNumberApp {

    public void isMultipleOfTen(int number) {
   	 // Check if the number is a multiple of 10 using the modulo operator (%)
        if ( number % 10 == 0) {
            System.out.println("The number is a multiple of 10");
        } else {
            System.out.println("The number is not a multiple of 10");
        }
    }
}

