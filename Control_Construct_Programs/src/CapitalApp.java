
public class CapitalApp {
		    public static void checkCharacter(char ch) {
		       // Check if the character is within the range of capital letters
		        if (ch >= 'A' && ch <= 'Z') {
		            System.out.println("Capital Letter");
		        } else {
		            System.out.println("Not a Capital Letter");
		        }
		    }
		}