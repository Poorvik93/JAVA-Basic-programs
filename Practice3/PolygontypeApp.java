
public class PolygontypeApp {

	public void Pollygon(int sides) {
		if (sides==3) {
			System.out.println("Triangle");
		}
		else if(sides==4) {
			System.out.println("Square");
		}
		else if (sides==5) {
			System.out.println("Pentagon");
		}
		else if (sides==6) {
			System.out.println("Hexagon");
		}
		else if (sides==7) {
			System.out.println("Heptagon");
		}
		else if (sides==8) {
			System.out.println("Octogon");
		}
		else if (sides==9) {
			System.out.println("Nanogon");
		}
		else if (sides==10) {
			System.out.println("Decogon");
		}
		else {
			System.out.println("Pollygon");
		}
	}

}
