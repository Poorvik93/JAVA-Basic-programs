// Java Program to Implement Type Casting of the Datatype

// Importing input output classes

// Main class
public class byteto {

    // Main driver method
    public static void main(String[] args) {

        // byte to byte ....
        byte a1 = 10;
        short b1;
        b1 = a1;
        System.out.println(a1);
        System.out.println(b1);
        System.out.println("byte to byte is  Casting is not required\n");

        // byte to short ....
        byte a2 = 10;
        short b2;
        b2 = a2;
        System.out.println(a2);
        System.out.println(b2);
        System.out.println("byte to short is implicit Casting\n");

        // byte to char....
        byte a3 = 10;
        char b3;
        b3 = (char) a3;
        System.out.println(a3);
        System.out.println(b3);
        System.out.println("byte to char is Explicit Casting\n");

        // byte to int ...
        byte a4 = 10;
        int b4;
        b4 = a4;
        System.out.println(a4);
        System.out.println(b4);
        System.out.println("byte to int is implicit Casting\n");

        // byte to long...
        byte a5 = 10;
        long b5;
        b5 = a5;
        System.out.println(a5);
        System.out.println(b5);
        System.out.println("byte to long is implicit Casting\n");

        // byte to float...
        byte a6 = 10;
        float b6;
        b6 = a2;
        System.out.println(a6);
        System.out.println(b6);
        System.out.println("byte to float is implicit Casting\n");

        // byte to double....
        byte a7 = 10;
        short b7;
        b7 = a2;
        System.out.println(a7);
        System.out.println(b7);
        System.out.println("byte to double is implicit Casting\n");

    }
}
